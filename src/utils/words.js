/**
 * Get today's word pair based on a reference epoch date.
 */
export function getTodaysPair() {
  const epoch = new Date("2025-01-01T00:00:00Z");
  const now = new Date();
  const daysSinceEpoch = Math.floor((now - epoch) / (1000 * 60 * 60 * 24));
  const index = ((daysSinceEpoch % WORD_PAIRS.length) + WORD_PAIRS.length) % WORD_PAIRS.length;
  return WORD_PAIRS[index];
}

/**
 * Get today's puzzle number (for display).
 */
export function getTodaysIndex() {
  const epoch = new Date("2025-01-01T00:00:00Z");
  const now = new Date();
  return Math.floor((now - epoch) / (1000 * 60 * 60 * 24)) + 1;
}
